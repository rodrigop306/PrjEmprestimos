package model;

public class Categoria {

	private int idCategoria=0;
	private String tipo="";

	public int getId() {
		return idCategoria;
	}

	public void setId(int id) {
		this.idCategoria = id;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

}
